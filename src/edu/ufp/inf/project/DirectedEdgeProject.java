package edu.ufp.inf.project;

import edu.princeton.cs.algs4.EdgeWeightedDigraph;
import edu.princeton.cs.algs4.StdOut;

/**
 *  The {@code DirectedEdge} class represents a weighted edge in an 
 *  {@link EdgeWeightedDigraph}. Each edge consists of two integers
 *  (naming the two vertices) and a real-value weight. The data type
 *  provides methods for accessing the two endpoints of the directed edge and
 *  the weight.
 *  <p>
 *  For additional documentation, see <a href="https://algs4.cs.princeton.edu/44sp">Section 4.4</a> of
 *  <i>Algorithms, 4th Edition</i> by Robert Sedgewick and Kevin Wayne.
 *
 *  @author Robert Sedgewick
 *  @author Kevin Wayne
 */

public class DirectedEdgeProject {
    private final int v;
    private final int w;
    private final double distancia;
    private final double tempoMedioPercursoMin;

    /**
     * Initializes a directed edge from vertex {@code v} to vertex {@code w} with
     * the given {@code weight}.
     * @param v the tail vertex
     * @param w the head vertex
     * @throws IllegalArgumentException if either {@code v} or {@code w}
     *    is a negative integer
     * @throws IllegalArgumentException if {@code weight} is {@code NaN}
     */
    public DirectedEdgeProject(int v, int w, double distancia, int tempoMedioPercursoMin ) {
        if (Double.isNaN(distancia) && Double.isNaN(tempoMedioPercursoMin)) throw new IllegalArgumentException("Weight is NaN");
        this.v = v;
        this.w = w;
        this.distancia = distancia;
        this.tempoMedioPercursoMin = tempoMedioPercursoMin;
    }

    /**
     * Returns the tail vertex of the directed edge.
     * @return the tail vertex of the directed edge
     */
    public int from() {
        return v;
    }

    /**
     * Returns the head vertex of the directed edge.
     * @return the head vertex of the directed edge
     */
    public int to() {
        return w;
    }

    public double getDistancia() {
        return distancia;
    }

    public double getTempoMedioPercursoMin() {
        return tempoMedioPercursoMin;
    }

    /**
     * Returns a string representation of the directed edge.
     * @return a string representation of the directed edge
     */
    public String toString() {
        return v + "->" + w + " " + String.format("Distancia: %5.2f | ", distancia) + String.format("Tempo Carro (Mais rapido): %5.2f", tempoMedioPercursoMin);
    }

    /**
     * Unit tests the {@code DirectedEdge} data type.
     *
     * @param args the command-line arguments
     */
    public static void main(String[] args) {
    }
}

